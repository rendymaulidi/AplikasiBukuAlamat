    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        String nama = textnama.getText();
        String alamat = textalamat.getText();
        String nomor = textnotelp.getText();

        if (!nama.isEmpty() && !alamat.isEmpty() && !nomor.isEmpty()) {
            tableModel.addRow(new Object[]{nama, alamat, nomor});
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!");
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        int selectedRow = tabelalamat.getSelectedRow();

        if (selectedRow != -1) {
            tableModel.setValueAt(textnama.getText(), selectedRow, 0);
            tableModel.setValueAt(textalamat.getText(), selectedRow, 1);
            tableModel.setValueAt(textnotelp.getText(), selectedRow, 2);
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah!");
        } 
    }//GEN-LAST:event_ubahActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int selectedRow = tabelalamat.getSelectedRow();

        if (selectedRow != -1) {
            tableModel.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
        }
    }//GEN-LAST:event_hapusActionPerformed

