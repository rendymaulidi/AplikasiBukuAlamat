    private void simpanKeFile(File file) {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            String nama = (String) tableModel.getValueAt(i, 0);
            String alamat = (String) tableModel.getValueAt(i, 1);
            String nomor = (String) tableModel.getValueAt(i, 2);
            writer.write(nama + "," + alamat + "," + nomor);
            writer.newLine();
        }
    } catch (IOException ex) {
        // Menangani exception di sini
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan: " + ex.getMessage());
        }
    }
    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
      File file = new File("data_alamat.txt");
    simpanKeFile(file);
    JOptionPane.showMessageDialog(this, "Alamat berhasil disimpan ke file!");
    
    }//GEN-LAST:event_simpanActionPerformed

    private void imporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imporActionPerformed
     JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Pilih file untuk diimpor");
    int userSelection = fileChooser.showOpenDialog(this);

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToOpen = fileChooser.getSelectedFile();
        importFromFile(fileToOpen);
    }
}

private void importFromFile(File file) {
    try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] data = line.split(",");
            if (data.length == 3) {
                tableModel.addRow(data);
            } else {
                JOptionPane.showMessageDialog(this, "Format data tidak valid: " + line);
            }
        }
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengimpor: " + ex.getMessage());
    }
    }//GEN-LAST:event_imporActionPerformed
    
